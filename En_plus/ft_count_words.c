#include <unistd.h>

int	ft_count_words(char *s)
{
	int	i;
	int count;

	i = 0;
	count = 0;
	while (s[i] != '\0')
	{
		if (s[i] != 32 && s[i] != 9)
		{
			count += 1;
			while (s[i] != '\0' && s[i] != 32 && s[i] != 9)
			{
				i++;
			}
		}
		else
		{
			i++;
		}
	}
	return (count);
}
